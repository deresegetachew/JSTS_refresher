/**
 *  NULL vs UNDEFINED
 * 
 * **/

//this will return undefined
let name;
console.log(name);

//this will return null
let age = null;
console.log(age);

//this is typeof object
console.log(typeof null);

console.log(typeof undefined);


//strict equality 
console.log(null === undefined);

//abstract equality
console.log(null == undefined);

